function buildHeroSlider() {
  const track = document.getElementById("hTrack");
  const dots  = document.getElementById("heroDots");
  heroRealCount = heroSlides.length;

  const allSlides = [heroSlides[heroRealCount-1], ...heroSlides, heroSlides[0]];

  track.innerHTML = allSlides.map(s => `
    <div class="hero-slide">
      <img src="${s.img}" alt="${s.title.replace(/<br>/g,' ')}" loading="lazy"
           onerror="this.style.display='none'">
      <div class="hero-content">
        <span class="hero-badge">${s.badge}</span>
        <h1>${s.title}</h1>
        <p>${s.desc}</p>
        <button class="btn-hero" onclick="event.stopPropagation();showDetail(${s.wisataId})">Jelajahi Sekarang →</button>
      </div>
    </div>`).join("");

  dots.innerHTML = heroSlides.map((_,i) =>
    `<div class="hero-dot${i===0?" active":""}" onclick="heroGo(${i})"></div>`
  ).join("");

  heroIdx = 0;
  heroSetPos(1, false);
}

function heroSetPos(trackIdx, animate) {
  const track = document.getElementById("hTrack");
  track.style.transition = animate ? "transform .55s cubic-bezier(.77,0,.175,1)" : "none";
  track.style.transform  = `translateX(-${trackIdx * 100}%)`;
}

function heroMove(d) {
  if (heroTransitioning) return;
  heroTransitioning = true;
  heroIdx += d;
  heroSetPos(heroIdx + 1, true);
  heroUpdateDots();
  heroStart();
}

