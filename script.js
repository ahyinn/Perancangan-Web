function isFbReady() { return window._fbReady === true; }

async function fbSaveData(path, data) {
  if (!isFbReady()) return false;
  try {
    const r = window._fbRef(window._fbDb, path);
    await window._fbPush(r, data);
    return true;
  } catch(e) { console.error("FB write error:", e); return false; }
}

async function fbSaveReview(wisataId, rev) {
  return fbSaveData(`reviews/${wisataId}`, { ...rev, ts: window._fbTimestamp() });
}

async function fbSaveContact(data) {
  return fbSaveData("contacts", { ...data, ts: window._fbTimestamp() });
}

function fbListenReviews(wisataId, cb) {
  if (!isFbReady()) { cb([]); return () => {}; }
  const path = window._fbRef(window._fbDb, `reviews/${wisataId}`);
  const unsub = window._fbOnValue(path, snap => {
    const data = snap.val();
    if (!data) { cb([]); return; }
    const arr = Object.values(data)
      .sort((a,b) => (b.ts||0) - (a.ts||0))
      .map(r => ({ ...r, ts: undefined }));
    cb(arr);
  });
  return unsub;
}

function fbGetAvg(wisataId) {
  return new Promise(res => {
    if (!isFbReady()) { res(null); return; }
    const path = window._fbRef(window._fbDb, `reviews/${wisataId}`);
    window._fbOnValue(path, snap => {
      const data = snap.val();
      if (!data) { res(null); return; }
      const arr = Object.values(data);
      res((arr.reduce((s,x) => s + (x.rating||0), 0) / arr.length).toFixed(1));
    }, { onlyOnce: true });
  });
}

