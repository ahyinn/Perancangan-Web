async function submitReview(wisataId) {
  if (!curUser) { openModal(); return; }
  if (!selRating) { toast("Pilih rating bintang terlebih dahulu"); return; }
  const text = document.getElementById("revText").value.trim();
  if (!text) { toast("Tulis ulasanmu terlebih dahulu"); return; }
  const rev = {
    name: curUser.name, email: curUser.email, rating: selRating, text,
    date: new Date().toLocaleDateString("id-ID", { day:"numeric", month:"long", year:"numeric" })
  };
  if (isFbReady()) {
    const ok = await fbSaveReview(wisataId, rev);
    if (!ok) { toast("Gagal menyimpan. Cek koneksi atau Firebase config."); return; }
  } else {
    lsSaveReview(wisataId, rev);
  }
  toast("Ulasan berhasil dikirim! Terima kasih");
  renderRevForm(curWisata);
  if (!isFbReady()) loadRevsList(wisataId);
}

function loadRevsList(wisataId) {
  const list    = document.getElementById("reviewsList");
  const empty   = document.getElementById("revEmpty");
  const loading = document.getElementById("revLoading");
  if (unsubReviews) { try { unsubReviews(); } catch {} unsubReviews = null; }
  if (isFbReady()) {
    loading.style.display = "block"; list.innerHTML = ""; empty.style.display = "none";
    unsubReviews = fbListenReviews(wisataId, reviews => {
      loading.style.display = "none";
      if (!reviews.length) { list.innerHTML = ""; empty.style.display = "block"; return; }
      empty.style.display = "none";
      list.innerHTML = reviews.map(revHTML).join("");
      const avg = (reviews.reduce((s,x) => s+x.rating, 0) / reviews.length).toFixed(1);
      document.getElementById("dRating").innerHTML = `<span style="color:#FBBF24">⭐ ${avg}</span><span>(${reviews.length} ulasan)</span>`;
    });
  } else {
    loading.style.display = "none";
    const reviews = lsGetReviews(wisataId);
    if (!reviews.length) { list.innerHTML = ""; empty.style.display = "block"; return; }
    empty.style.display = "none";
    list.innerHTML = reviews.map(revHTML).join("");
    const avg = lsAvg(wisataId);
    if (avg) document.getElementById("dRating").innerHTML = `<span style="color:#FBBF24">⭐ ${avg}</span><span>(${reviews.length} ulasan)</span>`;
  }
}

