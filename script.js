function startWisataListener() {
  if (!isFbReady()) return;
  const path = window._fbRef(window._fbDb, "wisata");
  window._fbOnValue(path, snap => {
    const data = snap.val();
    if (data && typeof data === "object") {
      const fromDb = Object.values(data).filter(Boolean);
      fromDb.sort((a, b) => (a.id || 0) - (b.id || 0));
      if (fromDb.length > 0) {
        wisata.length = 0;
        fromDb.forEach(w => wisata.push(w));
      }
    }
    if (curPage === "katalog") renderKatalog();
    if (curPage === "beranda") renderBeranda();
    if (curPage === "detail" && curWisata) {
      const updated = wisata.find(w => w.id === curWisata.id);
      if (!updated) { backToKatalog(); toast("Destinasi ini telah dihapus"); }
      else { curWisata = updated; }
    }
  });
}

