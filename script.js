function updateAuthUI() {
  const loggedIn = !!curUser;
  document.getElementById("userChip").style.display   = loggedIn ? "flex" : "none";
  document.getElementById("btnLogout").style.display  = loggedIn ? "inline-block" : "none";
  document.getElementById("btnLogin").style.display   = loggedIn ? "none" : "inline-block";
  if (loggedIn) {
    document.getElementById("userAv").textContent   = curUser.name.charAt(0).toUpperCase();
    document.getElementById("userName").textContent = curUser.name.split(" ")[0];
  }
  const mUser   = document.getElementById("mMobUser");
  const mLogin  = document.getElementById("mnav-login");
  const mLogout = document.getElementById("mnav-logout");
  if (loggedIn) {
    mUser.style.display  = "block";
    document.getElementById("mMobName").textContent  = curUser.name;
    document.getElementById("mMobEmail").textContent = curUser.email;
    mLogin.style.display  = "none";
    mLogout.style.display = "flex";
  } else {
    mUser.style.display   = "none";
    mLogin.style.display  = "flex";
    mLogout.style.display = "none";
  }
  if (curWisata) renderRevForm(curWisata);
}

